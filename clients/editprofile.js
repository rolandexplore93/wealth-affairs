
// function basic insturments checkbox
var expanded = false;

function BshowCheckboxes() {
  var checkboxes = document.getElementById("bInstrument");
  if (!expanded) {
    checkboxes.style.display = "block";
    expanded = true;
  } else {
    checkboxes.style.display = "none";
    expanded = false;
  }
}

